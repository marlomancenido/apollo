		<h3 style="font-size: 22px; border-bottom: 1px white solid; padding-bottom: 10px;">Synopsis</h3>
		<p>The show is set in and around a large medieval style castle, known as the "Kingdom of Fame" which rotates and manoeuvres throughout the show. A classical score accompanies the show's interludes, tying in with the show's dark, operatic theme. The show began with "Highway Unicorn (Road to Love)" accompanied by an extended intro, which Gaga performed while atop a fully puppeteer-ed horse, with a mechanical look, built by The Jim Henson Company, out of the three-story medieval castle prop. After the song, the stage went dark and spotlights were shone across the stage with a helicopter sound being played. The audience then hears "Mother G.O.A.T." speak for the first time stating that "alien fugitive Lady Gaga has escaped" and that "Operation: Kill the Bitch", is in order. Gaga comes out in an alien costume and sings "Government Hooker", in which she "seduces" a dancer who is dressed in a suit before killing him and leaving the stage. Gaga's dancers then reenact the birth scene akin to that in "Born This Way", as the singer moans and simulates giving birth after coming out of the zipper-like vagina of a large, inflatable body.
<br><br>
After "Black Jesus + Amen Fashion" and "Bloody Mary" an interlude commences and recites the opening dialogue of the music video of "Born This Way". For "Bad Romance", Gaga arrives on the stage inside a vessel. Following the song, she escapes to the top of the castle and sings "Judas", before escaping and performing "Fashion of His Love" and "Just Dance". During "LoveGame", a clear bathtub built on the stage inside which it is sung. After a speech from Gaga, thanking the audience for coming to her show, "Telephone" is performed with the original choreography. An interlude follows, with Mother G.O.A.T. describing "Gaga's take over of planet earth". "Heavy Metal Lover" is performed next and Gaga appears laying atop across the body of a motor-"tricycle" with her arms in two slots, a recreation of the album cover. Gaga drives around the Monster Pit followed by choreography for the last chorus of the song. After "Bad Kids" an acoustic section follows with songs like "Hair", "Princess Die" and "You and I". The section concludes with "Electric Chapel" with a display of flashing lights before Gaga leaves the stage.
<br><br>
After a Spanish guitar intro, she then returns in a recreation of the meat dress and performs "Americano". "Poker Face" is performed next and at the end of the song Gaga is lowered into a meat grinder. A moment later she emerges from the stage sitting on a meat couch and performs "Alejandro" with her gun bra and green trousers. The castle closes along an interlude and blue lights. Mother G.O.A.T. flies around the castle lip-syncing the words to the first verse of "Paparazzi" until Gaga returns to kill her with her new disco stick. Gaga then gives a speech about how there are no boundaries in music, and then performs "Scheiße". After the song the stage goes black. Moments later she returns in a tower singing "The Edge of Glory" and the last song, "Marry the Night". When the song ends, she and her dancers are lowered off stage and the castle goes dark.</p>
